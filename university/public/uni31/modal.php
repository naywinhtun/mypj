			<h5>Abstract</h5>
			<p>Intercritical heat treatments attempts were made to enhance the mechanical properties of reinforcing steel bars milled from scrap metal. For this, two grades of steel bars were obtained from different steel mills and their mechanical properties that include hardness, ultimate tensile strength, and percent elongation before and after intercritical heat treatment were determined. Results indicated that 25.5 and 17.6%, improvements in UTS (Ultimate Tensile Strength) and 18.8 and 14.3% improvement in percent elongation in two grades of reinforcing steel samples containing 0.17 and 0.24% carbon respectively was achieved while heating at 750oC for 2h. Appreciable improvement in the mechanical properties was noted due to birth of sufficient quantity of martensite along with ferrite</p>
